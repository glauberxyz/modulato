import fs from 'node:fs'
import { createRequire } from 'node:module'
import path from 'node:path'
import { pathToFileURL } from 'node:url'

/**
 * Load `.env` then `.env.local` into process.env before running the adapter.
 * `modulato content` is a plain Node process (not the Vite dev server), so
 * without this a credentialed adapter (Sanity, Contentful, …) never sees the
 * vars a developer put in a dotenv file — `pull()` gets `undefined`.
 * Precedence: a variable already in the real environment wins; among files
 * `.env.local` overrides `.env`. Tiny hand-rolled parser — no dependency.
 */
export function loadEnvFiles(root) {
  const fromRealEnv = new Set(Object.keys(process.env))
  for (const file of ['.env', '.env.local']) {
    const p = path.join(root, file)
    if (!fs.existsSync(p)) continue
    for (const line of fs.readFileSync(p, 'utf8').split('\n')) {
      const match = line.match(/^\s*(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*?)\s*$/)
      if (!match) continue
      const [, key, rawValue] = match
      if (fromRealEnv.has(key)) continue // an exported var beats any file
      let value = rawValue
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      )
        value = value.slice(1, -1)
      process.env[key] = value // .env.local (loaded second) overrides .env
    }
  }
}

/** Load modulato.config.ts through vite's TS-capable config loader. */
async function loadModulatoConfig(root) {
  const file = path.join(root, 'modulato.config.ts')
  if (!fs.existsSync(file))
    throw new Error(
      'no modulato.config.ts — create one with a content adapter, e.g.\n' +
        "  import { defineConfig } from 'modulato/config'\n" +
        "  import { localJson } from '@modulato/content-local'\n" +
        '  export default defineConfig({ content: localJson() })',
    )
  const require = createRequire(path.join(root, 'package.json'))
  const vite = await import(pathToFileURL(require.resolve('vite')).href)
  const result = await vite.loadConfigFromFile(
    { command: 'build', mode: 'production' },
    file,
    root,
  )
  return result?.config ?? {}
}

const IDENT = /^[A-Za-z_$][A-Za-z0-9_$]*$/

/** Infer a TypeScript type literal from a JSON value. */
function typeOf(value, indent) {
  if (value === null) return 'null'
  if (Array.isArray(value)) {
    if (!value.length) return 'unknown[]'
    const types = [...new Set(value.map((item) => typeOf(item, indent)))]
    const inner = types.join(' | ')
    return types.length === 1 ? `${inner}[]` : `Array<${inner}>`
  }
  switch (typeof value) {
    case 'string':
      return 'string'
    case 'number':
      return 'number'
    case 'boolean':
      return 'boolean'
    case 'object': {
      const entries = Object.entries(value)
      if (!entries.length) return 'Record<string, unknown>'
      const pad = '  '.repeat(indent + 1)
      const fields = entries
        .map(([key, val]) => {
          const name = IDENT.test(key) ? key : JSON.stringify(key)
          return `${pad}${name}: ${typeOf(val, indent + 1)}`
        })
        .join('\n')
      return `{\n${fields}\n${'  '.repeat(indent)}}`
    }
    default:
      return 'unknown'
  }
}

function generateTypes(snapshot) {
  const fields = Object.entries(snapshot)
    .map(([key, value]) => {
      const name = IDENT.test(key) ? key : JSON.stringify(key)
      return `    ${name}: ${typeOf(value, 2)}`
    })
    .join('\n')
  return `// Generated by \`modulato content\` — do not edit. Regenerate after
// changing your content source.
// (Augments the module that DECLARES ModulatoContent — merging only works
// at the declaration site, not on re-exports.)
declare module 'modulato/config' {
  interface ModulatoContent {
${fields}
  }
}

export {}
`
}

/**
 * Pull the content source into .modulato/content.json + content.d.ts.
 * The snapshot feeds page loaders (typed as \`content\`); commit it so
 * builds are reproducible without content-source credentials.
 */
export async function pullContent(root, { typegen = true } = {}) {
  loadEnvFiles(root)
  const config = await loadModulatoConfig(root)
  const adapter = config.content
  if (!adapter || typeof adapter.pull !== 'function')
    throw new Error(
      'modulato.config.ts has no content adapter — set `content: localJson()` (from @modulato/content-local) or another adapter.',
    )

  const snapshot = await adapter.pull({ root })
  if (!snapshot || typeof snapshot !== 'object')
    throw new Error(`adapter "${adapter.name}" returned ${typeof snapshot}, expected an object`)

  const outDir = path.join(root, '.modulato')
  fs.mkdirSync(outDir, { recursive: true })
  fs.writeFileSync(
    path.join(outDir, 'content.json'),
    JSON.stringify(snapshot, null, 2) + '\n',
  )
  const files = ['.modulato/content.json']
  // Build-time refreshes skip typegen (`{ typegen: false }`): a build only needs
  // the snapshot, and rewriting content.d.ts in CI is a pointless, dirtying write.
  if (typegen) {
    fs.writeFileSync(path.join(outDir, 'content.d.ts'), generateTypes(snapshot))
    files.push('.modulato/content.d.ts')
  }

  return { adapter: adapter.name, keys: Object.keys(snapshot), files }
}

/**
 * Build-time content refresh, called at the start of `modulato build`. Pulls a
 * fresh snapshot (content.json only — no typegen) so a deploy ships current
 * content. Gated: opt in with `refetchOnBuild: true` in modulato.config, or force
 * it with `--refetch`; `--no-content` (skip) always wins. A pull failure is caught
 * and reported — a content-source outage must never break a deploy (the build
 * falls back to the committed snapshot).
 *
 * Returns { status: 'skipped' | 'disabled' | 'refreshed' | 'failed', … }.
 */
export async function refreshContentForBuild(root, { force = false, skip = false } = {}) {
  if (skip) return { status: 'skipped' }
  if (!force) {
    // Read only the flag here — the adapter isn't used, so env need not be loaded.
    try {
      const config = await loadModulatoConfig(root)
      if (config.refetchOnBuild !== true) return { status: 'disabled' }
    } catch {
      return { status: 'disabled' } // no config → nothing to refresh
    }
  }
  try {
    // pullContent loads .env then the config, so the adapter sees its credentials.
    const result = await pullContent(root, { typegen: false })
    return { status: 'refreshed', adapter: result.adapter, keys: result.keys }
  } catch (err) {
    return { status: 'failed', error: err instanceof Error ? err.message : String(err) }
  }
}
